/**
 * DataModel MW-specific meta item.
 *
 * @class
 * @abstract
 * @extends ve.dm.AlienMetaItem
 * @constructor
 * @param {Object} element Reference to element in meta-linmod
 */
ve.dm.MWGraphMetaItem = function VeDmMWGraphMetaItem( element ) {
	// Parent constructor
	ve.dm.AlienMetaItem.call( this, element );
};

/* Inheritance */

OO.inheritClass( ve.dm.MWGraphMetaItem, ve.dm.AlienMetaItem );

/* Static Properties */

ve.dm.MWGraphMetaItem.static.name = 'mwGraphMeta';

ve.dm.MWGraphMetaItem.static.matchTagNames = [];

// MWGraphMetaItems are generated by ve.dm.MWTransclusionNode#toDataElement when
// all of the transclusions contents are considered to be metadata or whitespace

/* Registration */

ve.dm.modelRegistry.register( ve.dm.MWGraphMetaItem );
